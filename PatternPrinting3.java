package chapter2;

public class PatternPrinting3 {

	public static void main(String[] args) {
		  int n = 5;

	        for (int i = 0; i < n; i++) {
	            // Print spaces
	            for (int j = 0; j < i; j++) {
	                System.out.print(" ");
	            }

	            // Print star
	            System.out.println("*");
	        }
	    }
	

	}


